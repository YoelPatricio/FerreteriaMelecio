    public void cargarDatos() {
        
        if(ventaDetalleList==null){
            return;
        }
        
        List<VentaDetalle> datos = ventaDetalleList;

        Object[][] matriz = new Object[datos.size()][5];
        
        for (int i = 0; i < datos.size(); i++) {
            
            
            //System.out.println(s[0]);
            
            matriz[i][0] = datos.get(i).getProducto().getIdProducto();
            matriz[i][1] = datos.get(i).getProducto().getNombre();
            matriz[i][2] = datos.get(i).getPrecioUnitario();
            matriz[i][3] = datos.get(i).getCantidad();
            matriz[i][4] = datos.get(i).getTotal();
            
            

        }
        Object[][] data = matriz;
        String[] cabecera = {"Código", "Producto","Precio Unitario", "Cantidad","Total S/"};
        dtm = new DefaultTableModel(data, cabecera);
        tblDatos.setModel(dtm);
    }

