    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        VentaDetalle vd = new VentaDetalle();
        
        Producto p = new Producto();
        p.setIdProducto(Integer.parseInt(txtIdProducto.getText()));
        p.setNombre(txtNombreProducto.getText());
        
        vd.setProducto(p);
        vd.setPrecioUnitario(new BigDecimal(txtPrecioUnitario.getText()));
        vd.setCantidad(Integer.parseInt(txtCantidad.getText()));
        vd.setTotal((new BigDecimal(txtPrecioUnitario.getText())).multiply(new BigDecimal(txtCantidad.getText())));
        
        int entregado = 0;
        Date fechaEntrega = null;
        if(!cboEntregar.getSelectedItem().toString().equalsIgnoreCase("NO")){
            entregado = 1;
            fechaEntrega = new Date();
        }
        
        vd.setEntregado(entregado);
        vd.setFechaEntrega(fechaEntrega);
        
        ventaDetalleList.add(vd);
        cargarDatos();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        int selection = tblDatos.getSelectedRow();
        if(selection>=0){
            ventaDetalleList.remove(selection);
            cargarDatos();
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un registro!");
        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

